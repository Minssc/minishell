#include "minishell.h"

int	ft_isspace(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] < 9 || str[i] > 13)
		{
			if (str[i] == 32)
				continue ;
			return (0);
		}
	}
	return (1);
}
